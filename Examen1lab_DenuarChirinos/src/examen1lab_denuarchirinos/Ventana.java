            libros.get(pos).setEdicion(edicion);
            libros.get(pos).setGenero(genero);
            libros.get(pos).setPuntaje(puntaje);
            libros.get(pos).setTitulo(titulo);
            libros.get(pos).setValor(valor);
            libros.get(pos).setnCopias(nCopias);
            DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_posLibro.getModel();
            DefaultComboBoxModel modelo2 = (DefaultComboBoxModel) cb_carrito.getModel();
            modelo2=modelo;
            libros.add(libro);        
            //reinicio variables:
            txt_titulo1.setText("");
            txt_descripcion1.setText("");
            cb_puntaje1.setSelectedIndex(0);
