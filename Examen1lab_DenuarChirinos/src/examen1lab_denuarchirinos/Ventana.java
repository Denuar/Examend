    private void btn_aceptarLogUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarLogUsuarioActionPerformed
        // TODO add your handling code here:
        nombreUlog=txt_nombreUlog.getText();
        contraseñaUlog=txt_passUlog.getText();
        if(usuarios.size()==0){
            JOptionPane.showMessageDialog(null, "Deben registrarse usuarios primero");
        }
        for (int i = 0; i<usuarios.size(); i++) {
            if (usuarios.get(i).getNombreU().equals(nombreUlog) && usuarios.get(i).getContraseña().equals(contraseñaUlog)){
                logUsuario=1;
                JOptionPane.showMessageDialog(null, "Usuario Autenticado Correctamente\n"
                        + "Bienvenido " + usuarios.get(i).getNombreC());
            }
            else if (logUsuario==0 && i==usuarios.size()-1){
                JOptionPane.showMessageDialog(null, "Nombre de Usuario o Contraseña Incorrecto");
            }
        }        
        //renicio variables:
        txt_nombreUlog.setText("");
        txt_passUlog.setText("");
        //fin reinicar variables
    }//GEN-LAST:event_btn_aceptarLogUsuarioActionPerformed
